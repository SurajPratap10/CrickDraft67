function pool(format: TournamentFormat): Squad[] {
  return SQUADS.filter((s) => s.format === format);
}

export function getFormatStats(format: TournamentFormat) {
  const squads = pool(format);
  const nations = [...new Set(squads.map((s) => s.nation))];
  const players = squads.reduce((sum, s) => sum + s.players.length, 0);
  return { nations: nations.length, squads: squads.length, players };
}

