import type { Player, Role, Squad, TournamentFormat } from '../types';
import { buildExtraSquads } from './extra-squads';

const p = (number: number, name: string, roles: Role[], rating: number): Player => ({
  id: `${name.toLowerCase().replace(/\s+/g, '-')}-${number}`,
  number,
  name,
  roles,
  rating,
});

const squad = (
  nation: string,
  flag: string,
  tournament: string,
  year: number,
  players: Player[],
): Squad => {
  const format: TournamentFormat = tournament.startsWith('T20') ? 't20' : 'odi';
  const slug = nation.toLowerCase().replace(/\s+/g, '-');
  return {
    id: `${slug}-${format}-${year}`,
    nation,
    flag,
    tournament,
    format,
    year,
    players,
  };
};

