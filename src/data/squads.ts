export const SQUAD_COUNT = SQUADS.length;
export const PLAYER_COUNT = SQUADS.reduce((sum, s) => sum + s.players.length, 0);

function pickRandom<T>(items: T[]): T | null {
  if (items.length === 0) return null;
  return items[Math.floor(Math.random() * items.length)];
}

/** Fresh draw — any unused squad in selected format */
export function getRandomSquad(format: TournamentFormat, usedSquadIds: string[] = []): Squad | null {
  const available = pool(format).filter((s) => !usedSquadIds.includes(s.id));
  return pickRandom(available);
}

/** Reroll: different nation, same format */
export function getAnotherNationSquad(
  format: TournamentFormat,
  currentNation: string,
  _currentSquadId: string,
  usedSquadIds: string[] = [],
): Squad | null {
  const available = pool(format).filter(
    (s) => s.nation !== currentNation && !usedSquadIds.includes(s.id),
  );
  return pickRandom(available);
}

/** Reroll: same nation, different World Cup year, same format */
export function getSameNationOtherWorldCup(
  format: TournamentFormat,
  nation: string,
  currentSquadId: string,
  usedSquadIds: string[] = [],
): Squad | null {
  const available = pool(format).filter(
    (s) => s.nation === nation && s.id !== currentSquadId && !usedSquadIds.includes(s.id),
  );
  return pickRandom(available);
}

export function countNationAlternatives(
  format: TournamentFormat,
  nation: string,
  currentSquadId: string,
  usedSquadIds: string[],
): number {
  return pool(format).filter(
    (s) => s.nation === nation && s.id !== currentSquadId && !usedSquadIds.includes(s.id),
  ).length;
}

export function countOtherNationAlternatives(
  format: TournamentFormat,
  currentNation: string,
  usedSquadIds: string[],
): number {
  return pool(format).filter(
    (s) => s.nation !== currentNation && !usedSquadIds.includes(s.id),
  ).length;
}
